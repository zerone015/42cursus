char	*ft_strchr(const char *str, int value)
{
	size_t	i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if (*(str + i) == value)
			return (str + i);
		i++;
	}
	if (value == '\0')
		return (str + i);
	return (0);
}
