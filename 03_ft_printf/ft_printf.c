#include "ft_printf.h"

int	ft_printf(const char *format, ...)
{
	va_list	ap;
	int		print_len;
	t_info	info;

	va_start(ap, format);
	init_array(&info);
	print_len = parse_format(ap, format, &info);
	va_end(ap);
	return (print_len);
}
